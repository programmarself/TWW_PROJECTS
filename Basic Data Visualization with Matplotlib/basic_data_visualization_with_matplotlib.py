# Import libraries
import matplotlib.pyplot as plt
import numpy as np

# Sample data
x = np.linspace(0, 10, 100)
y = np.sin(x)

# Plotting
plt.figure(figsize=(10, 6))

# Line plot
plt.subplot(2, 1, 1)
plt.plot(x, y, label='Sine Wave')
plt.title('Line Plot')
plt.xlabel('X-axis')
plt.ylabel('Y-axis')
plt.legend()

# Scatter plot
plt.subplot(2, 1, 2)
plt.scatter(x, y, color='red', label='Points')
plt.title('Scatter Plot')
plt.xlabel('X-axis')
plt.ylabel('Y-axis')
plt.legend()

plt.tight_layout()
plt.show()



